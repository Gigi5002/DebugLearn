# Давай возьмём простой пример с багом, чтобы потренироваться. Вот код:

numbers = [1, 2, 3, "4", 5]  # Тут строка "4" — потенциальная проблема
total = 0
for num in numbers:
    total += num
print("Сумма:", total)

# Поставь точку останова (breakpoint)
#
#     Кликни левой кнопкой мыши слева от строки с кодом (например, на строке total += num). Появится красная точка — это место, где код остановится в дебаг-режиме.
#     Это нужно, чтобы посмотреть, что происходит внутри цикла.
#
# Запусти дебаг-режим
#
#     Найди вверху значок жука ("Debug") рядом с зелёным треугольником. Жми на него (или Shift + F9).
#     PyCharm запустит код и остановится на первой точке останова.
#
# Что ты увидишь
#
#     Внизу откроется панель отладки. Там будут переменные (numbers, num, total) и их значения на момент остановки.
