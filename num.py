def calculate_average(numbers2):
    total2 = 0
    for num2 in numbers2:
        total2 += num2
    average = total2 / len(numbers2)
    return average


data = [10, 20, 30, "40", 50]
result = calculate_average(data)
print("Среднее:", result)

